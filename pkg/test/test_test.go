func Test_Matrix_Body_Mpbody_Move(t *testing.T) {
	var b *Mpbody
	{
		b = &Mpbody{
			a: 4,
			z: 0,
			m: map[uint16]object.Object{
				4: {X: 4, Y: 4},
				3: {X: 3, Y: 3},
				2: {X: 2, Y: 2},
				1: {X: 1, Y: 1},
				0: {X: 0, Y: 0},
			},
		}
	}

	{
		b.Move()
	}

	if b.m[5].X != 0 {
		t.Fatal("expected", 0, "got", b.m[5].X)
	}
	if b.m[5].Y != 0 {
		t.Fatal("expected", 0, "got", b.m[5].Y)
	}

	fmt.Printf("%#v\n", b)
}

